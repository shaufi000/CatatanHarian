private void importDataFromFile(String filePath) {
    try (BufferedReader reader = new BufferedReader(new FileReader(filePath))) {
        StringBuilder data = new StringBuilder();
        String line;
        // Membaca setiap baris dari file dan menambahkannya ke StringBuilder
        while ((line = reader.readLine()) != null) {
            data.append(line).append("\n");
        }
        // Menetapkan teks ke JTextArea catatanHarian
        taCatatanHarian.setText(data.toString());
    } catch (IOException e) {
        // Menampilkan pesan kesalahan jika gagal mengimpor data
        JOptionPane.showMessageDialog(this, "Gagal mengimpor data.", "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}

private void exportDataToFile(String filePath) {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
        // Menulis teks dari JTextArea catatanHarian ke file
        writer.write(taCatatanHarian.getText());
        // Menampilkan pesan berhasil jika berhasil meng export data
        JOptionPane.showMessageDialog(this, "Berhasil diekspor ke file: " + filePath, "Sukses", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException e) {
        // Menampilkan pesan gagal jika gagal melakukan export data
        JOptionPane.showMessageDialog(this, "Gagal mengekspor data.", "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}

