    private void btnEksportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEksportActionPerformed
        // TODO add your handling code here:
        // Membuat objek fileChooser untuk memilih tempat menyimpanan file yang telah kita buat ke folder yang ingin kita gunakan untuk menyimpan data
        JFileChooser fileChooser = new JFileChooser();
    int result = fileChooser.showSaveDialog(this);
    
    // Mendapatkan file yang dipilih oleh user
    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();
        
        // Menambahkan ekstensi .txt agar data tersimpan dengan format .txt
        String filePath = selectedFile.getAbsolutePath();
        if (!filePath.endsWith(".txt")) {
            filePath += ".txt";
        }
        
        // Memanggil metode exportDataToFile untuk mengekspor data kedalam folder
        exportDataToFile(filePath);
    }
    }//GEN-LAST:event_btnEksportActionPerformed

    private void btnImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImportActionPerformed
        // TODO add your handling code here:
        // Membuat objek fileChooser untuk memilih file yang ingin diimpor ke dalam aplikasi
        JFileChooser fileChooser = new JFileChooser();
    int result = fileChooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        // Mendapatkan file yang dipilih oleh user
        File selectedFile = fileChooser.getSelectedFile();
        
        // Memanggil metode importDataFromFile untuk mengimpor data dari file ke dalam aplikasi
        importDataFromFile(selectedFile.getAbsolutePath());
    }
    }//GEN-LAST:event_btnImportActionPerformed

